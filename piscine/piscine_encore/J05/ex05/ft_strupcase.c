#include <stdio.h>
#include <unistd.h>

char	*ft_strupcase(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'a') && (str[i] <= 'z'))
		{
			str[i] = str[i] - 32;
		}
		i++;
	}
	return(str);
}

int		main(int argc, char **argv)
{
	char *str;

	str = ft_strupcase(argv[1]);
	printf("%s",str);
	return (0);
}
